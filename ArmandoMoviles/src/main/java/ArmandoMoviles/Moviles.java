package ArmandoMoviles;

public class Moviles {
	int varillasNecesarias=0;
	int pesoTotal=0;
	

	public int getVarillasNecesarias() {
		return varillasNecesarias;
	}

	public void setVarillasNecesarias(int varillasNecesarias) {
		this.varillasNecesarias = varillasNecesarias;
	}

	public int getPesoTotal() {
		return pesoTotal;
	}

	public void setPesoTotal(int pesoTotal) {
		this.pesoTotal = pesoTotal;
	}

}